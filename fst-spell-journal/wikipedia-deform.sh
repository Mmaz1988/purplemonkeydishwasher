#!/bin/bash
sed -e 's/#REDIRECT.*//g' \
    -e 's/#redirect.*//g' \
    -e 's/\[\[...\?:[^]]*\]\]//g' \
    -e 's/<[^>]*>//g' \
    -e 's/\&lt;.*\&gt;//' \
    -e 's/\[\[[^]|]*|//g' \
    -e 's/\]\]//g' \
    -e 's/{{[^}]*}}//g' \
    -e "s/'''*//g" \
    -e 's/^{\?|.*//g' \
    -e 's/^\[\[[a-z]*:.*//g' \
    -e 's/\&amp;nbsp;/ /g' \
    -e 's/\&quot;/"/g' \
    -e 's/[a-z]*=[a-z0-9]*//g' \
    -e 's/^[!:-].*//g' \
    -e 's/Category://g' |\
    tr -s "[=|]{}()<>&;*" " "
